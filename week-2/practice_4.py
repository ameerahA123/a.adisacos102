tuple = ("Ekiti", 750, 'Oshogbo', 250, "Akure", 500)
s_tuple = ("Abeokuta", 300, "Ogbomoso")

# Prints the complete tuple
print(tuple)

# Prints last element of the tuple
print(tuple[-1])

# Prints elements of the tuple starting from 3rd till 4th (index 2 and 3)
print(tuple[2:4])

# Prints elements of the tuple starting from 4th element to the end (index 3 onwards)
print(tuple[3:])

# Prints the contents of s_tuple three times
print(s_tuple * 3)

# Prints concatenated tuples
print(tuple + s_tuple)
